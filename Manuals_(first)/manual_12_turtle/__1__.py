import turtle


t = turtle.Pen()

t.forward(200)
t.backward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)

t.shape('turtle')
# circle, square, triangle, arrow, turtle

t.color('green')
# red, green, blue, white, black, violet ...

t.pensize(4)

turtle.mainloop()
